package com.ex;

public class operationEx1 {
	public static void main(String[] args)
	{
		int a=22;
		int b=20;
		
		int resultValue=a+b;
		System.out.println(resultValue);
		
		resultValue=a-b;
		System.out.println(resultValue);
		
		resultValue=a/b;
		System.out.println(resultValue);
		
		resultValue=a*b;
		System.out.println(resultValue);
		
		resultValue=a%b;
		System.out.println(resultValue);
		
	}
}
