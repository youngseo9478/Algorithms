package com.ex;

public class OperationEx2 {	//대입연산자
	public static void main(String[] args)
	{
		int a=5;
		
		a+=2;
		System.out.println("a+=2 = >" +a);
		
		a-=1;
		System.out.println("a-=2 = >" +a);
		
		a*=3;
		System.out.println("a*=3 = >" +a);
		
		a/=2;
		System.out.println("a/=2 = >" +a);
		
		a%=2;
		System.out.println("a%=2 = >" +a);
		
	}

}
